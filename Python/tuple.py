# Tuple:- A built in data type that lets us create immutable sequence of value

tup = (2,1,3,1)
print(tup[0]) # 2
print(type(tup)) # <class 'tuple'>


tup = (1,2,3)
print(tup[0]) # 1
print(tup[1]) # 2


tup=()
print(tup) #()
print(type(tup)) #<class 'tuple'>

tup=(1,)
print(tup) # 1,
print(type(tup)) #<class 'tuple'> if comma (,) nhi lga rhta to type int aata


