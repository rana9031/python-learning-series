# slicing
# accessing parts of a string
str = "APNA COLLEGE"
print(str[1:4]) # PNA
print(str[5:12]) #COLLEGE
print(str[5:len(str)]) #COLLEGE
print(str[5:]) #COllege
print(str[:4]) #APNA

# slicing in negative index

str = "APPLE"
print(str[-3:-1]) #PL
print(str[-5:-2]) #APP
print(str[-5:-1])
